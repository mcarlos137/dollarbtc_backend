package com.dollarbtc.backend.cryptocurrency.exchange.dto.service.mfa;

import java.io.Serializable;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author CarlosDaniel
 */
@XmlRootElement
public class MFASendCodeRequest implements Serializable, Cloneable {
    
    private String userName, language;
    private boolean sendSms, sendMail, mcNewUser;
    
    public MFASendCodeRequest() {
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }
    
    public boolean isSendSms() {
        return sendSms;
    }

    public void setSendSms(boolean sendSms) {
        this.sendSms = sendSms;
    }

    public boolean isSendMail() {
        return sendMail;
    }

    public void setSendMail(boolean sendMail) {
        this.sendMail = sendMail;
    }

    public boolean isMcNewUser() {
        return mcNewUser;
    }

    public void setMcNewUser(boolean mcNewUser) {
        this.mcNewUser = mcNewUser;
    }

}
