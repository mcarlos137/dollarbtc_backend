package com.dollarbtc.backend.cryptocurrency.exchange.dto.service.debitcard;

import java.io.Serializable;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author CarlosDaniel
 */
@XmlRootElement
public class DebitCardCreateNewPinRequest implements Serializable, Cloneable {

    private String id, secretKey, pin;
    
    public DebitCardCreateNewPinRequest() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSecretKey() {
        return secretKey;
    }

    public void setSecretKey(String secretKey) {
        this.secretKey = secretKey;
    }

    public String getPin() {
        return pin;
    }

    public void setPin(String pin) {
        this.pin = pin;
    }    
    
}
