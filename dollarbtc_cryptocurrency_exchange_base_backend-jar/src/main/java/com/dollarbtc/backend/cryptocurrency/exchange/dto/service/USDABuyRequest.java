package com.dollarbtc.backend.cryptocurrency.exchange.dto.service;

import java.io.Serializable;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author CarlosDaniel
 */
@XmlRootElement
public class USDABuyRequest implements Serializable, Cloneable {
    
    private String userName;
    private Double amount, price;
    private boolean marketPrice;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public Double getAmount() {
        return amount;
    }

    public void setAmount(Double amount) {
        this.amount = amount;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public boolean isMarketPrice() {
        return marketPrice;
    }

    public void setMarketPrice(boolean marketPrice) {
        this.marketPrice = marketPrice;
    }    
    
}
