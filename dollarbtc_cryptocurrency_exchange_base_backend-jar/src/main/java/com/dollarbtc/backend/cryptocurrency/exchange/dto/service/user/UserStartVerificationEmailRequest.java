package com.dollarbtc.backend.cryptocurrency.exchange.dto.service.user;

import java.io.Serializable;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author CarlosDaniel
 */
@XmlRootElement
public class UserStartVerificationEmailRequest implements Serializable, Cloneable {
    
    private String userName, info;
    
    public UserStartVerificationEmailRequest() {
    }

    public UserStartVerificationEmailRequest(String userName, String info) {
        this.userName = userName;
        this.info = info;
    }
    
    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

}
